<h1 style="text-align: justify">Origem do Arduino?</h1>
<p>Como nasceu o Arduino? Para que serve um Arduino? Quais as vantagens? Como eu começo a programar? Nesse tutorial vamos apresentar um resumo sobre o que é Arduino e como você pode utilizá-lo em seus projetos.</p>
<h2 style="text-align: justify">O que é Arduino</h2>
<p>O Arduino foi criado em 2005 por um grupo de 5 pesquisadores : <strong> Banzi, David Cuartielles, Tom Igoe, Gianluca Martino e David Mellis</strong>. O objetivo era elaborar um dispositivo que fosse ao mesmo tempo barato, funcional e fácil de programar, sendo dessa forma acessível a estudantes e projetistas amadores. Além disso, foi adotado o conceito de hardware livre, o que significa que qualquer um pode montar, modificar, melhorar e personalizar o Arduino, partindo do mesmo hardware básico.</p>
<p>Assim, foi criada uma placa composta por um <strong>microcontrolador Atmel</strong>, circuitos de entrada/saída e que pode ser facilmente conectada à um computador e programada via IDE (Integrated Development Environment, ou Ambiente de Desenvolvimento Integrado) utilizando uma linguagem baseada em C/C++, sem a necessidade de equipamentos extras além de um cabo USB.</p>
<img src = "img/arduino2.png">
<p>Depois de programado, o microcontrolador pode ser usado de forma independente, ou seja, você pode colocá-lo para controlar um robô, uma lixeira, um ventilador, as luzes da sua casa, a temperatura do ar condicionado, pode utilizá-lo como um aparelho de medição ou qualquer outro projeto que vier à cabeça.</p>
<p>No vídeo abaixo você vai descobrir o que é e para o que serve o <strong> Confira!</strong></p>

<div style="text-align:center "><iframe width="800" height="400" src="https://www.youtube.com/embed/Ve2NJSyqVRA" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
