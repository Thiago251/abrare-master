<div style="margin-top: 30px;"><h2 style= "text-align: justify;">O que você pode fazer com o Arduino</h2></div>
<p>A lista de possibilidades é praticamente infinita. Você pode automatizar sua casa, seu carro, seu escritório, criar um novo brinquedo, um novo equipamento ou melhorar um já existente. Tudo vai depender da sua criatividade.</p>
<p>Para isso, o Arduino possui uma quantidade enorme de sensores e componentes que você pode utilizar nos seus projetos. Grande parte do material utilizado está disponível em módulos, que são pequenas placas que contém os sensores e outros componentes auxiliares como resistores, capacitores e leds.</p>
<img src = "img/arduino3.png">
<p>Existem também os chamados Shields, que são placas que você encaixa no Arduino para expandir suas funcionalidades. A imagem abaixo mostra um Arduino Ethernet Shield encaixado no Arduino Mega 2560. Ao mesmo tempo que permite o acesso à uma rede ou até mesmo à internet, mantém os demais pinos disponíveis para utilização, assim você consegue, por exemplo, utilizar os pinos para receber dados de temperatura e umidade de um ambiente, e consultar esses dados de qualquer lugar do planeta:</p>
<img src = "img/arduino4.png">
<div style="text-align: justify;"><h2>Modelos de Placas Arduino</h2></div>
<p>O tipo de placa que você vai utilizar depende muito do projeto a ser desenvolvido e o número de portas necessárias. As opções vão das mais comuns, como o Arduino Uno e suas 14 portas digitais e 6 analógicas, passando por placas com maior poder de processamento, como o <strong>Arduino Mega</strong>, com <strong>microcontrolador ATmega2560</strong> e 54 portas digitais, e o <strong>Arduino Due</strong>, baseado em <strong>processador ARM</strong> de 32 bits e 512 Kbytes de memória:</p>
<img src = "img/arduino5.png">
<div style="text-align: justify;"><h2>Estrutura de um programa em Arduino</h2> </div>
<p>Escrever um programa em Arduino é muito simples. Tudo o que você precisa é conectá-lo ao computador por meio de um cabo USB e utilizar um ambiente de programação chamado IDE, onde você digita o programa, faz os testes para encontrar eventuais erros e transfere o programa para o dispositivo.</p>
<p>Na imagem abaixo temos a IDE já com um programa carregado. No site <a href="https://www.arduino.cc/en/Guide"target="_blank"class="stilo">oficial do Arduino</a> você pode fazer o download da IDE gratuitamente:</p>
<img src = "img/arduino6.png" width= "800" height="700">
<br>
<h2 style="text-align: justify;">Por que Arduino?</h2>
<p>Graças à sua experiência de usuário simples e acessível, o Arduino foi usado em milhares de projetos e aplicativos diferentes. O software Arduino é fácil de usar para iniciantes, mas flexível o suficiente para usuários avançados. Ele roda em Mac, Windows e Linux. Professores e alunos o usam para construir instrumentos científicos de baixo custo, para provar os princípios da química e da física ou para iniciar a programação e a robótica. Designers e arquitetos constroem protótipos interativos, músicos e artistas usam para instalações e para experimentar novos instrumentos musicais. Os criadores, é claro, o usam para construir muitos dos projetos exibidos na Maker Faire, por exemplo. Arduino é uma ferramenta fundamental para aprender coisas novas. Qualquer pessoa - crianças, hobistas, artistas, programadores - pode começar a mexer apenas seguindo as instruções passo a passo de um kit,</p>
<p>Existem muitos outros microcontroladores e plataformas de microcontroladores disponíveis para computação física. Parallax Basic Stamp, Netmedia's BX-24, Phidgets, MIT's Handyboard e muitos outros oferecem funcionalidade semelhante. Todas essas ferramentas pegam os detalhes complicados da programação do microcontrolador e os envolvem em um pacote fácil de usar. O Arduino também simplifica o processo de trabalho com microcontroladores, mas oferece algumas vantagens para professores, alunos e amadores interessados ​​em relação a outros sistemas:</p>
<p><strong>Barato</strong> - as placas Arduino são relativamente baratas em comparação com outras plataformas de microcontrolador. A versão mais barata do módulo Arduino pode ser montada manualmente, e mesmo os módulos Arduino pré-montados custam menos de  R$50.</p>
<p><strong>Plataforma cruzada</strong> - O software Arduino (IDE) é executado nos sistemas operacionais Windows, Macintosh OSX e Linux. A maioria dos sistemas de microcontroladores está limitada ao Windows.</p>
<p><strong>Ambiente de programação simples e claro</strong> - O Arduino Software (IDE) é fácil de usar para iniciantes, mas flexível o suficiente para usuários avançados aproveitarem. Para professores, ele é convenientemente baseado no ambiente de programação de processamento, de modo que os alunos que estão aprendendo a programar naquele ambiente estarão familiarizados com o funcionamento do IDE do Arduino.</p>
<p><strong>Software de código aberto e extensível</strong> - O software Arduino é publicado como ferramentas de código aberto, disponíveis para extensão por programadores experientes. A linguagem pode ser expandida por meio de bibliotecas C ++, e as pessoas que desejam entender os detalhes técnicos podem dar o salto do Arduino para a linguagem de programação AVR C na qual ela se baseia. Da mesma forma, você pode adicionar o código AVR-C diretamente em seus programas Arduino, se desejar.</p>
<p><strong>Hardware extensível e de código aberto</strong> - Os planos das placas Arduino são publicados sob uma licença Creative Commons, de modo que designers de circuito experientes podem fazer sua própria versão do módulo, estendendo-o e aprimorando-o. Mesmo usuários relativamente inexperientes podem construir a versão breadboard do módulo para entender como ele funciona e economizar dinheiro.</p>
